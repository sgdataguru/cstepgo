async function seedAttractions() {
  console.log('ðŸŽ¯ Starting attractions seed...');

  let created = 0;
  let skipped = 0;

  for (const attraction of attractionsData) {
    try {
      await prisma.attraction.upsert({
        where: {
          name: attraction.name,
        },
        update: {},
        create: {
          ...attraction,
        },
      });
      created++;
      console.log(`âœ… Created: ${attraction.name} (Zone ${attraction.zone})`);
    } catch (error) {
      // If attraction already exists (unique constraint on name), skip it
      skipped++;
      console.log(`â­ï¸  Skipped: ${attraction.name} (already exists)`);
    }
  }

  console.log('\nðŸŽ‰ Attractions seeded successfully!');
  console.log('\nðŸ“Š Summary:');
  console.log(`- Created: ${created}`);
  console.log(`- Skipped: ${skipped}`);
  console.log(`- Total: ${attractionsData.length}`);
  
  // Count by zone
  const zoneACounts = attractionsData.filter(a => a.zone === 'A').length;
  const zoneBCounts = attractionsData.filter(a => a.zone === 'B').length;
  const zoneCCounts = attractionsData.filter(a => a.zone === 'C').length;
  
  console.log('\nðŸ—ºï¸  By Zone:');
  console.log(`- Zone A (City Center): ${zoneACounts} attractions`);
  console.log(`- Zone B (Suburban): ${zoneBCounts} attractions`);
  console.log(`- Zone C (Regional): ${zoneCCounts} attractions`);
}

seedAttractions()
  .catch((e) => {
    console.error('âŒ Error seeding attractions:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
